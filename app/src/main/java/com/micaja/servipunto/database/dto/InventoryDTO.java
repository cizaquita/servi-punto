package com.micaja.servipunto.database.dto;

public class InventoryDTO implements DTO
{
	
	private String inventoryId,productId,quantity,uom;
	private Integer syncStatus;
	private Double quantityBalance;

	public String getInventoryId() {
		return inventoryId;
	}

	public void setInventoryId(String inventoryId) {
		this.inventoryId = inventoryId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getUom() {
		return uom;
	}

	public void setUom(String uom) {
		this.uom = uom;
	}

	public Integer getSyncStatus() {
		return syncStatus;
	}

	public void setSyncStatus(Integer syncStatus) {
		this.syncStatus = syncStatus;
	}

	public Double getQuantityBalance () {return quantityBalance;}

	public void setQuantityBalance (Double quantityBalance) {this.quantityBalance = quantityBalance;}

}
